/**
 * 1. Given a string firstName equals to "Den" and lastName equals to "Brown". Print to the console the full name in
 * the following format: "-FirstName=LastName-"
 *
 */

public class FirstLastName {
    public static void main(String[] args) {
        String firstName = "Den";
        String lastName = "Brown";
        System.out.println("-" + firstName+ "=" + lastName + "-");
    }
}
